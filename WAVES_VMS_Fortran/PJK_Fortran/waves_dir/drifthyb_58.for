	FUNCTION FLILO(X,B) 
C 
C 
C       FLILO MEANS LINEAR FUNCTION FOR SMALL ARGUMENT
C       AND LOGARITHM FOR LARGE ARGUMENT
C       SIGN IS PRESERVED 
C       FOR /X/.LT.B   FLILO=X
C       FOR /X/.GT.B   FLILO=A*LOG(X)+C IN SUCH A WAY 
C       THAT FLILO AND ITS DERIVATIVE ARE CONTINUOUS AT /X/=/B/ 
C 
        XM=ABS(X) 
        IF(XM.GT.B) GO TO 1 
	 FLILO=X
        RETURN
    1   FT=B*ALOG(XM/B)+B 
        FLILO=SIGN(FT,X)
        RETURN          
	END 
         FUNCTION AFLILO(X,B) 
C 
C       INVERSE OF FLILO
C 
        XM=ABS(X) 
        IF(XM.GT.B) GO TO 1 
        AFLILO=X
        RETURN
    1   FT=B*EXP(XM/B-1.) 
        AFLILO=SIGN(FT,X) 
        RETURN
        END 
      SUBROUTINE LONGWK(W,DEQ,DMAG) 
C     THIS ROUTINE EVALUATES THE DISPERSION RELATION FOR LOWER HYBRID
C	DRIFT WAVES, E.G. DAVIDSON AND GLADD, PHYS.FL.18,1327,(1975)
C       FOR THE LONG WAVELENGTH LIMIT
C     NC IS THE NUMBER OF COMPONENTS, FOR NOW ONLY WORKS FOR 2
C     RME(N) IS THE RATIO OF MASS(N) TO ELECTRON MASS, BUT IS 
C     NEGATIVE IF CHARGE IS NEGATIVE
C     DENS(N) IS RATIO OF DENSITY TO SOME STANDARD DENSITY, USUALLY 
C     DENSITY OF COMPONENT (1)  
C     BETAZ(N) IS VZ/C FOR COMPONENT N
C     THE MAGNETIC FIELD IS SPECIFIED BY GIVING FCEHZ
C 
      COMMON /BEAMBL/ XD(80),VTH(10),WC(10),WP2(10),NC,RPT2
     1  ,WP(10),WT,WCE,RP,EOM
      DIMENSION RME(10),DENS(10),TEMPEV(10),BETAZ(10)
	EQUIVALENCE (XD(1),XKZ),(XD(2),XKY),(XD(3),XKX) 
	EQUIVALENCE (XD(9),FHZCE),(XD(10),FHZPE)
	EQUIVALENCE (XD(5),WR),(XD(6),WI),(XD(7),RW)
	EQUIVALENCE (XD(8),AZL)
	EQUIVALENCE (XD(11),E)              ! E IS IN VOLTS/METER 
	EQUIVALENCE (XD(12),DDNDX)          ! 1/N DN/DX IS IN CM**-1
	EQUIVALENCE (XD(21),RME(1)),(XD(31),DENS(1)),(XD(51),TEMPEV(1))
	EQUIVALENCE (XD(41),BETAZ(1)) 
      COMPLEX W,DEQ,ZETAI,XIE,XII,DZRI,RPT2,RIMZ,RINZ,RKMZ,RKNZ
      REAL MEC2
	DATA TWOPI /6.2831853/	
	DATA MEC2 /.51E6/	!M C**2 FOR ELECTRONS IN EV
	DATA QE /4.807E-10/	!ELECTRON CHARGE IN ESU
	DATA C /2.9979E10/	!VELOCITY OF  LIGHT IN CGS
C
	VESU = C*1.E-8			!300 = VOLTS TO ESU
	EPS0 = 1.E11/(2.*TWOPI*C**2)

C 
	SMALLB = (C*XKY/WC(1))**2*TEMPEV(1)/MEC2
C	XKYRL = SQRT(SMALLB)
	VE = -.01*EOM*E/FHZCE/TWOPI/VESU
        CALL BESIZ(CMPLX(SMALLB,0.),0,2,1,RIMZ,RINZ,RKMZ,RKNZ,LERR) 
C
C	THE FOLLOWING INCLUDES ONLY THE DENSITY GRADIENT, NOT B OR TEMP
C
	VDELTA = -C**2*(TEMPEV(1)/MEC2/TWOPI/FHZCE)*RIMZ*EXP(-SMALLB)
     1   *DDNDX
C	PRINT*,'VE,VDELTA',VE,VDELTA
	ZETAI = W/XKY/VTH(2)
	ZETAIR = ZETAI
	ZETAII = AIMAG(ZETAI)
	CALL Z(ZETAIR,ZETAII,ZR,ZI,DZR,DZI)
	DZRI = CMPLX(DZR,DZI)
	XII = -WP2(2)/(XKY*VTH(2))**2*DZRI
	DEQ = 1. + XII
	DMAG = 1. + CABS(XII)
	XIE = (WP2(1)/WC(1)**2)*(1. - RIMZ*EXP(-SMALLB))/SMALLB
     1   + 2.*WP2(1)/(XKY*VTH(1))**2*(XKY*VDELTA/(W - XKY*VE))
	DEQ = DEQ + XIE
	DMAG = DMAG + CABS(XIE)
C	PRINT*,'RIMZ,SMALLB,VTH(1)',RIMZ,SMALLB,VTH(1)
C	PRINT*,'XKY,WP2(1),WC(1)',XKY,WP2(1),WC(1)
C	PRINT*,'XIE,XII',XIE,XII
C	PRINT*,'W,DEQ',W,DEQ
      RETURN
      END 
      SUBROUTINE SORTN(IXS,FOLLOW,NS) 
      REAL IXS(20),IXT
      DIMENSION FOLLOW(20)
C     ORDER X COORDS IN INCREASING ORDER
      DO 11 N = 1,NS-1
      N1 = N + 1
      DO 12 M = N1,NS 
      IF((IXS(M)-IXS(N)).GT.0) GO TO 12 
C     EXCHANGE
      IXT = IXS(N)
      IXS(N) = IXS(M) 
      IXS(M) = IXT
      IXT = FOLLOW(N) 
      FOLLOW(N) = FOLLOW(M) 
      FOLLOW(M) = IXT 
   12 CONTINUE
   11 CONTINUE
      RETURN
      END 
      FUNCTION CRECIP(X)
C 
C  THIS FUNCTION RETURNS THE COMPLEX RECIPROCAL OF X
C  IT AVOIDS THE PROBLEMS OF SQUARING LARGE NUMBERS 
C 
      COMPLEX X,CRECIP
      BNORM = X 
      BNORM = ABS(BNORM) + ABS(AIMAG(X))
      IF(BNORM.LT.1.E-30) GO TO 1 
      BNORM = 1./BNORM
      CRECIP = X*BNORM
      CRECIP = BNORM/CRECIP 
      RETURN
    1 CRECIP = 1.E30
      RETURN
      END
