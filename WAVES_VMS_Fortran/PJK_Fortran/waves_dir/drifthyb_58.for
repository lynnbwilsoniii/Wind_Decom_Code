	CALL MGOAXIS(BL,-YMINT,-1.,-1.,GX1,GY,GX1,GY1,100,0)
	CALL MGORELOCATE(GX1,-BY)
	CALL MGOAXIS(BL,-YMINT,-1.,-1.,GX2,GY,GX2,GY1,100,1)
