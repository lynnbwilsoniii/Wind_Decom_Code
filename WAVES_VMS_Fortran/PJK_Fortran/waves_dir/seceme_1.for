	PROGRAM SECEME
C
C	CALCULATES AVERAGE VALUE OF 1/SQRT(ES) FOR SECONDARY
C	EMISSION FROM COPPER, SEAH, SURFACE SCIENCE 17,132-160,(1969)
C
	DELE = .05
	VAL1 = 0.
	VAL2 = 0.
	DO N = 1,1000
	ES = .05*N
	FSR = 1 + (N.AND.1)
	Y = ES/(ES +.35)/(ES + 4.5)**1.6
	VAL1 = VAL1 + DELE*FSR*Y
	VAL2 = VAL2 + DELE*FSR*Y/SQRT(ES)
	ENDDO
	PRINT*,'VAL1,VAL2,AVR SQRT(ES)',VAL1,VAL2,VAL1/VAL2
	STOP
	END

