	PROGRAM EDECAY
C
C	ELECTROSTATIC DECAY OF LANGMUIR WAVE
C
	REAL K0,K1,K2,K1P,K1M,K2P,K2M
	DATA TWOPI /6.28318531/
C
	FP = 22500.
	EB = 5.			! KEV
	VB = SQRT(2.*EB/.511E3)*3.E8			! M/SEC
	TE = 10.					! EV		
	TI = 10.					! EV		
	VS = SQRT((TE + 3.*TI)/.511E6/1836.)*3.E8       ! M/SEC
	VT2 = TE*(3.E8)**2/.511E6
C
	K0 = TWOPI*FP/VB
	W02 = (TWOPI*FP)**2 + 3.*VT2*K0**2
	W0 = SQRT(W02)
	print*,'k0,w0',k0,w0
C
C	QUADRATIC FOR K1 = DAUGHTER LANGMUIR WAVE
C	ONE OF THE SOLUTIONS WILL BE K1 = K0 AND K2 = 0.  THE OTHER
C		ONE IS THE DESIRED (NONTRIVIAL) SOLUTION
C
	AA = 3.*VT2 - VS**2
	BB = -2.*VS*(W0 - K0*VS)
	CC = (TWOPI*FP)**2 - (W0-K0*VS)**2
	DISC = BB**2 - 4.*AA*CC
	K1P = .5*(-BB+SQRT(DISC))/AA	
	K1M = .5*(-BB-SQRT(DISC))/AA	
C
	PRINT*,AA,BB,CC,DISC
	PRINT*,'k1+, k1-',K1P,K1M
	K2M = K0 - K1M
	K2P = K0 - K1P
	PRINT*,'k2+, k2-',K2P,K2M
	STOP
	END
