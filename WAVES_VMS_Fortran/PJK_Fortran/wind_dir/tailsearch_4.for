	program tailsearch
C
C	READS FILE FOR077.DAT;2 OF ORBIT DATA AND SEARCHS FOR TIMES WHEN 
C	WIND IS BEYOND TLIM 
C
	DATA TLIM /-10./
C
C	OPEN(UNIT=78,FILE='FOR077.DAT;2',STATUS='OLD',READONLY)
	OPEN(UNIT=78,FILE='ORBIT.TABLE',STATUS='OLD',READONLY)
 100	CONTINUE
	READ(78,*,END=200) DAY,NDAY,NHR,XGSE,YGSE,ZGSE,RRE
	IF(XGSE.GT.TLIM) GO TO 100
	WRITE(79,1077) DAY,NDAY,NHR,XGSE,YGSE,ZGSE,RRE
 1077	FORMAT(F8.0,I10,I8,4F8.2)
	GO TO 100
C
 200	CONTINUE
	STOP
	END

