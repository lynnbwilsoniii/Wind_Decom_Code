	PROGRAM FFTFREQ
C
C	SET FACTOR FOR 4 FREQUENCIES PER OCTAVE
C
C	SAMPLING RATES FOR WIND ARE 21860, 5465,AND 341
C	FOR FUNDAMENTAL FREQUENCIES OF 21.348, 5.337, AND .3336 HZ
C
	FFHI = 21860./1024.
	FFMID = FFHI/4.
	FFLO = FFMID/16.
C
	PRINT*,'FUNDAMENTAL FREQS',FFHI,FFMID,FFLO
C
	FRAT = .5**.25
	FREQHI = 511.
	DO N = 1,28
	  FREQLO = FREQHI*FRAT
	  NF2 = FREQHI 
	  NF1 = FREQLO + 1.
	  FREQAV = .5*(FREQHI+FREQLO)
	  FREQAVN = .5*(NF1+NF2)
	  PRINT*,' '
	  PRINT 1001,N,FREQAV,FREQAVN,NF1,NF2,FFHI*FREQAVN,
     1      FFMID*FREQAVN,FFLO*FREQAVN
	  FREQHI = FREQLO
	ENDDO
	DO N = 29,31
	  FREQAV = 32-N
	  PRINT*,' '
	  PRINT 1001,N,FREQAV,FREQAV,32-N,32-N,FFHI*FREQAV,
     1    FFMID*FREQAV,FFLO*FREQAV
	ENDDO
	STOP
 1001	FORMAT(I4,F10.3,F10.3,I8,I8,2X,F11.2,F11.3,F11.4)
	END
