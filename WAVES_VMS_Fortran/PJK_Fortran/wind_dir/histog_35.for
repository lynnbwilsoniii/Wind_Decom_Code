	PROGRAM HISTOG
C
C	MAKES A HISTOGRAM OF NOISE DATA
C
	CHARACTER*120 JUNK(10)
	CHARACTER*14 TITLE(10)
	DIMENSION NDATA(2060),NHIST(512)
	DATA NHIST/512*0/
C
	TITLE(8) = 'SINCAL28.DAT'
	PRINT*,TITLE(8)
	OPEN(UNIT=19,READONLY,NAME=TITLE(8),TYPE='OLD')
C	OPEN(UNIT=19,NAME='NOISE2.DAT',TYPE='OLD')
 1001	FORMAT(I4,1X,20I5)
 1002	FORMAT(A)
	DO IL = 1,5
	  READ(19,1002) JUNK(IL)
	  PRINT*,IL,JUNK
	ENDDO
C
	DO IL = 1,103
	  IK2 = 20*IL
	  IK1 = IK2-19
	  READ(19,1001,END=200) LINNO,(NDATA(IK),IK=IK1,IK2)
C	  PRINT*,LINNO,NDATA(IK1),NDATA(IK2)
	ENDDO
200	CONTINUE
C	PRINT*,LINNO,NDATA(2048)
C
C	FIND MAX AND MIN, FOR POS AND NEG
	NMAXP = 0
	NMINP = 256
	NMAXN = 0
	NMINN = 256
	DO IL = 1,2048
	  IF(NDATA(IL).GT.0) THEN
	    NMAXP = MAX0(NMAXP,NDATA(IL))
	    NMINP = MIN0(NMINP,NDATA(IL))
	  ELSE
	    NMAXN = MAX0(NMAXN,-NDATA(IL))
	    NMINN = MIN0(NMINN,-NDATA(IL))
	  ENDIF
	ENDDO
	PRINT*,'MAXP,MINP,-MAXN,-MINN',NMAXP,NMINP,NMAXN,NMINN
	DO IL = 1,2048
	  IH = NDATA(IL)
	  NHIST(IH) = NHIST(IH)+1
	ENDDO
	WRITE(25,1003) (IH-128,NHIST(IH),IH=1,256)
 1003	FORMAT(2I6)
	STOP
	END
