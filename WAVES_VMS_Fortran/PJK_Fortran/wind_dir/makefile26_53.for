	SUBROUTINE SOLFIT(NPT,ENV,A,B,C)
C
C	FITS A/COSH((N-B)/C) TO ENV
C
	REAL ENV(1026)
C
C	VERY QUICK AND DIRTY
	MID = NPT/2
	A = .5*(ENV(MID) + ENV(MID+1))
C
C	B IS WEIGHTED AVERAGE OF N
C
	AVRN = 0.
	AVRX = 0.
	DO N = 1,NPT
	  AVRN = AVRN +   ENV(N)
	  AVRX = AVRX + N*ENV(N)
	ENDDO
	B = AVRX/AVRN
	ENDAV = .5*(ENV(1) + ENV(NPT))
	PRINT*,'SOLFIT,A,ENDAV',A,ENDAV
	Y = A/ENDAV + SQRT((A/ENDAV)**2 - 1.)
	C = (MID-.5)/ALOG(Y)
	print*,'SOLFIT,ENDAV,EST',ENDAV,A/COSH((MID-.5)/C)
	print*,'SOLFIT,A,B,C',A,B,C
	RETURN
	END
	SUBROUTINE SOLERR(X,SUMSQ)
C
	REAL X(25)
	COMMON /SOLBLK/ NPT,ENV(1026)
C
	C1 = 0.
	C2 = 0.
	C3 = 0.
	B = X(1)
	C = X(2)
	DO N=1,NPT
	  C1 = C1 + ENV(N)**2
	  COSHFN = COSH((N-B)/C)
	  C2 = C2 + ENV(N)/COSHFN
	  C3 = C3 + 1./COSHFN**2
	ENDDO
	X(3) = C2/C3
	SUMSQ = C1 - 2.*X(3)*C2 + C3*X(3)**2
	RETURN
