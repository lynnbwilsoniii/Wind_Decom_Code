C		OF FREQUENCY (FANDBW?)  that didnt work either.
C  
C	NEW PROCEEDURE:  LOOK FOR REGULARLY MODULATED ENVELOPES.  
C	FIND SPECTRAL PEAK (SR FINDMAIN),  USE THIS TO DECIDE HOW MANY 
C	SAMPLES TO SEARCH FOR ENVELOPE. (TWO CYCLES)   THEN CONSTRUCT A SERIES 
C	OF ENVELOPE VALUES.  FOURIER ANALYZE THIS TO FIND IF MODULATION IS 
C	REGULAR OR NOT.  THEN, AS IT IS POSSIBLE TO HAVE MODULATION DUE ONLY
C	TO BEATING WITH SAMPLE RATE, CHECK SPECTRUM TO SEE IF IT AGREES.
C
