	program SPIKEHIST
C
C	MAKES TABLES FOR HISTOGRAM OF SUNCLOCK
C		AT THE MOMENT, ONLY HISTOGRAMS OF XCOUNT,YCOUNT
C
	INTEGER SUNCLOCK,NHISTX(2048),NHISTY(2048)
	DATA NHISTX /2048*0/
	DATA NHISTY /2048*0/
C
	OPEN(UNIT=37,FILE='FOR037.DAT',STATUS='OLD',READONLY)
 151	CONTINUE
	READ(37,*,END=161,ERR=151) NO_EVT,XMAX,YMAX,XCOUNT,YCOUNT
	NXC = XCOUNT
	NYC = YCOUNT
	MAXX = 100.*XMAX
	MAXY = 100.*YMAX
	NHISTX(NXC+1) = NHISTX(NXC+1) + 1
	NHISTY(NYC+1) = NHISTY(NYC+1) + 1
	GO TO 151
 161	CLOSE(UNIT=37)
	DO N = 1,2048
 	  WRITE(67,*) N,NHISTX(N),NHISTY(N)
	  NHISTX(N) = 0
	  NHISTY(N) = 0
	ENDDO
	STOP
	END
