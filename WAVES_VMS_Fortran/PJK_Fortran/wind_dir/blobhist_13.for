	program blobhist
C
C	MAKES TABLES FOR HISTOGRAM OF SUNCLOCK FOR BLOBS
C
	INTEGER SUNCLOCK,NHIST(4096)
	DATA NHIST /4096*0/
C
	OPEN(UNIT=51,FILE='FOR051.DAT',STATUS='OLD',READONLY)
 151	CONTINUE
	READ(51,*,END=161,ERR=151) NO_EVT,AVR1,ZMAX,SUNCLOCK
	NHIST(SUNCLOCK+1) = NHIST(SUNCLOCK+1) + 1
	GO TO 151
 161	CLOSE(UNIT=51)
	DO N = 1,4096
 	  WRITE(61,*) N,NHIST(N)
	  NHIST(N) = 0
	ENDDO
	OPEN(UNIT=52,FILE='FOR052.DAT',STATUS='OLD',READONLY)
 152	CONTINUE
	READ(52,*,END=162,ERR=152) NO_EVT,AVR1,ZMAX,SUNCLOCK
	NHIST(SUNCLOCK+1) = NHIST(SUNCLOCK+1) + 1
	GO TO 152
 162	CLOSE(UNIT=52)
	DO N = 1,4096
 	  WRITE(62,*) N,NHIST(N)
	  NHIST(N) = 0
	ENDDO
	OPEN(UNIT=53,FILE='FOR053.DAT',STATUS='OLD',READONLY)
 153	CONTINUE
	READ(53,*,END=163,ERR=153) NO_EVT,AVR1,ZMAX,SUNCLOCK
	NHIST(SUNCLOCK+1) = NHIST(SUNCLOCK+1) + 1
	GO TO 153
 163	CLOSE(UNIT=53)
	DO N = 1,4096
 	  WRITE(63,*) N,NHIST(N)
	  NHIST(N) = 0
	ENDDO
	STOP
	END
