	PROGRAM ORDER64
C
C	READS A FILE FOR064.DAT OF TDS EVENTS AT SHOCK CROSSINGS AND PUTS 
C		THEM IN CHRONOLOGICAL ORDER
C
	INTEGER*4 NEVT(500)
	CHARACTER*80 JUNK(500)
	CHARACTER*15 FILENAME
C
	NFILE = 0
 300	CONTINUE
	WRITE(FILENAME,1003) NFILE
	PRINT*,'NFILE= ',NFILE
	PRINT 1004, FILENAME
 1003	FORMAT(1X,'FOR064.DAT;',I2)
 1004	FORMAT(A)
C	OPEN(UNIT=64,FILE='FOR064.DAT',STATUS='OLD',READONLY)
	OPEN(UNIT=64,FILE=FILENAME,STATUS='OLD',READONLY)
	NP = 0
 100	CONTINUE
	NP = NP+1
	READ(64,1001,END=200) NEVT(NP),JUNK(NP)
C	print 1002,np,nevt(np),junk(np)
 1001	FORMAT(I10,A60)
 1002	FORMAT(I5,I10,A60)
C	IF(NP.GT.10) GO TO 200
	GO TO 100
 200	CONTINUE
	CLOSE(UNIT=64)
	CALL SORTN(NEVT,JUNK,NP)
	DO N = 1,NP
C	  print 1002,n,nevt(n),junk(n)
	  WRITE(65,1001) NEVT(N),JUNK(N)
	ENDDO
	NFILE = NFILE-1
	CLOSE(UNIT=65)
	GO TO 300
 400	STOP
	END
      SUBROUTINE SORTN(IXS,FOLLOW1,NS)
C
      INTEGER*4 IXS(1),IXT
      CHARACTER*80 CXT, FOLLOW1(1)
C
C     ORDER X COORDS IN INCREASING ORDER
C
      DO 11 N = 1,NS-1
      N1 = N + 1
      DO 12 M = N1,NS
      IF((IXS(M)-IXS(N)).GT.0.) GO TO 12
C     EXCHANGE
      IXT = IXS(N)
      IXS(N) = IXS(M)
      IXS(M) = IXT
      CXT = FOLLOW1(N)
      FOLLOW1(N) = FOLLOW1(M)
      FOLLOW1(M) = CXT
   12 CONTINUE
   11 CONTINUE
      RETURN
      END
