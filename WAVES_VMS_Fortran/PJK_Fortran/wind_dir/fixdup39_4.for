	program fixdup39
C
C	READS IN THE VOLUMINOUS RESULTS OF MAKEFILE39 AND CHECKS FOR 
C	DUPLICATION
C
	INTEGER  SCETI4(2),ERTDAY,NO_EVT,MAXCH1,MAXCH2
	CHARACTER*2  EVENT
	CHARACTER*120 JUNK
	INTEGER NOEVTSTORE(3000)
C
	OPEN(UNIT=90,FILE='MAKEFILE39.RESULTS',STATUS='OLD')
	READ(90,1001) JUNK
	WRITE(80,1001) JUNK
	READ(90,1001) JUNK
	WRITE(80,1001) JUNK
	NSTORE = 0
 1001	FORMAT(A)
 100	CONTINUE
        READ(90,1054,END=200) SCETI4,ERTDAY,EVENT,NO_EVT,MAXCH1,MAXCH2,
     1          E1MAX,E2MAX,EEIG1,EEIG2,FREQKHZ,FP,FCE 
 1054   FORMAT(I10,I7,I3,A2,I10,2I4,4E11.3,3F8.2)
 1055   FORMAT(I10,I7,I10,I3,A2,2I4,4E11.3,3F8.2)   	
	NSTORE = NSTORE+1
C
	IWRITE = 1
	DO N = 1,NSTORE-1
	  IF(NO_EVT.EQ. NOEVTSTORE(N)) IWRITE = 0 
	ENDDO
	NOEVTSTORE(NSTORE) = NO_EVT
C	
        IF(IWRITE.GE.1) write(80,1055)SCETI4,NO_EVT,ERTDAY,EVENT,MAXCH1,
     1          MAXCH2,E1MAX,E2MAX,EEIG1,EEIG2,FREQKHZ,FP,FCE
	GO TO 100
 200	CONTINUE 
	PRINT*,'TOTAL EVENTS ',NSTORE
C	RELEVANT LINE FROM TDSVIS
C        READ(44,1114,END=200) SCETR,NEVTR,NTMDAY,EVTR
C 1114   FORMAT(I10,I7,I10,I3,A2,3F7.0,F7.2,F6.2,F7.0,F6.1,3F7.1,2F7.1  
	STOP
	END

