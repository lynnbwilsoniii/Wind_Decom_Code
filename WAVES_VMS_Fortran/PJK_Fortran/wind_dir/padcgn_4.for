	FUNCTION XPADC(F)
C
C	THIS FUNCTION RETURNS THE GAIN OF THE DC CHANNEL OF THE WIND X PREAMP
C	NOT TESTED YET
C
	COMPLEX CGAIN,Y1,Z1,Y2,Z2
	DATA R1,C1,R2,CINP /5.E8,22.E-12,5.E8,4.51E-12/
	DATA R3,C3 /2.2E+03,1.E-9/
	DATA TWOPI /6.2831853/
C
	W = TWOPI*F
	CGAIN = 1.
C	DIVISION OF SIGNAL BEFORE FOLLOWER
	Y1 = CMPLX(1./R1,W*C1)
	Y2 = CMPLX(1./R2,W*CINP)
	Z1 = 1./Y1
	Z2 = 1./Y2
	CGAIN = CGAIN*Z2/(Z1+Z2)
C	EFFECT OF OUTPUT RC
	WC3 = 1./(W*C3)
	CGAIN = CGAIN*CMPLX(0.,WC3)/CMPLX(R3,WC3)
	XPADC = CABS(CGAIN)
	RETURN
	END
