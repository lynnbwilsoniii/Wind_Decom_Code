	PROGRAM LOOP
C
C	PLOTS A FIGURE OF LOOP AND SEARCH COIL NOISE
C
	DIMENSION GIMP6(9),FIMP6(9)
	DIMENSION GNVB06(6),FNVB06(6)
	DIMENSION GISEE2(16),FISEE2(16)
	DIMENSION GISEE3(13),FISEE3(13)
	DIMENSION GGEOS(7),FGEOS(7)
	DIMENSION PLDATA(20,2)
	CHARACTER*30 COM(20)
C
C     NOISE DATA (G) IN GAMMA/ROOT(HZ), FREQ (F) IN HZ
C     FROM RODRIGUEZ AND GURNETT JGR 81,2870?,(1976)
	DATA FIMP6 /   10.,   30., 100.,1000., 3000.,  1.E4,
     1   5.E4,1.E5,2.E5/
	DATA GIMP6 / 4.E-4,1.2E-4,6.E-5,6.E-6,1.2E-6,4.5E-7,
     1  3.E-7,2.2E-7,4.5E-7/
C	DATA FOR NVB-06
	DATA FNVB06 /   3.E5,  6.E5,  1.E6, 1.5E6  ,2.E6,  3.E6/
	DATA GNVB06 / 1.6E-6,7.3E-7,4.6E-7,2.9E-7,2.9E-7,7.3E-7/
C	DATA FOR ISEE 2 = 10 KHZ SEARCH COIL
	DATA FISEE2 /    5.6,    10.,  17.8,  31.1,   56.2,   100.,
     1    178.,     311.,    562.,   1000.,  1780.,  3110.,  5620.,
     2  10000.,  17800.,   31100./
	DATA GISEE2 / 5.3E-3,2.18E-3,8.5E-4,4.1E-4,1.47E-4,5.67E-5,
     1  3.89E-5, 2.63E-5, 1.52E-5,1.07E-5, 1.02E-5,9.05E-6,9.95E-6,
     2  8.31E-6, 1.87E-5, 1.55E-4/
C	DATA FOR ISEE 3 = 1 KHZ SEARCH COIL
	DATA FISEE3 /  1.,  1.78, 3.11,   5.6,    10.,  17.8,  31.1, 
     1   56.2 ,   100.,  178.,     311.,  562.,  1000./
	DATA GISEE3/5.E-2,2.E-3,7.7E-4,4.0E-4,2.2E-4,1.26E-4,7.1E-5,
     1  4.5E-5, 2.9E-5, 2.5E-5 ,2.3E-5 ,2.3E-5, 2.5E-5/
C	DATA FOR GEOS-VLF
	DATA FGEOS /   30.,  100.,  300.,1000.,  3000., 10000., 30000./
	DATA GGEOS /2.7E-4,3.3E-5,1.0E-5,4.5E-6,3.0E-6, 3.2E-6, 1.0E-5/
C
C	COM(1) = 'TERMINAL 3'
	COM(1) = 'PRINTER 6'
	COM(2) = 'LIMITS 0.  7.  -8.  -1.'
	COM(3) = 'EXPAND .7'
	CALL MONGO(3,COM,2,3,PLDATA)
	DO 10 N = 1,9
	PLDATA(N,1) = FIMP6(N)
	PLDATA(N,2) = GIMP6(N)
  10	CONTINUE
	COM(1) = 'LINES 1 9'
	COM(2) = 'INPUT LOOP.MGO'
	COM(3) = 'LABEL IMP-6'
	CALL MONGO(3,COM,20,2,PLDATA)
C
	DO 20 N = 1,6
	PLDATA(N,1) = FNVB06(N)
	PLDATA(N,2) = GNVB06(N)
  20	CONTINUE
	COM(1) = 'LINES 1 6'
	COM(2) = 'INPUT LOOP.MGO'
	COM(3) = 'LABEL NVB02'
	CALL MONGO(3,COM,20,2,PLDATA)
C
	DO 30 N = 1,16
	PLDATA(N,1) = FISEE2(N)
	PLDATA(N,2) = GISEE2(N)
  30	CONTINUE
	COM(1) = 'LINES 1 16'
	COM(2) = 'INPUT LOOP.MGO'
	COM(3) = 'LABEL ISEE-2'
	CALL MONGO(3,COM,20,2,PLDATA)
C
	DO 40 N = 1,13
	PLDATA(N,1) = FISEE3(N)
	PLDATA(N,2) = GISEE3(N)
  40	CONTINUE
	COM(1) = 'LINES 1 13'
	COM(2) = 'INPUT LOOP.MGO'
	COM(3) = 'LABEL ISEE-3'
	CALL MONGO(3,COM,20,2,PLDATA)
C
	DO 50 N = 1,7
	PLDATA(N,1) = FGEOS(N)
	PLDATA(N,2) = GGEOS(N)
  50	CONTINUE
	COM(1) = 'LINES 1 7'
	COM(2) = 'INPUT LOOP.MGO'
	COM(3) = 'LABEL GEOS'
	CALL MONGO(3,COM,20,2,PLDATA)
C
	COM(1) = 'BOX'
	COM(2) = 'EXPAND .7'
	COM(3) = 'XLABEL LOG FREQUENCY'
	COM(4) = 'YLABEL LOG GAMMA/ROOT(HZ)'
	COM(5) = 'ID'
	COM(6) = 'HARDCOPY'
	COM(7) = 'END'
C	COM(5) = 'END'
	CALL MONGO(6,COM,20,2,PLDATA)
	STOP
	END

