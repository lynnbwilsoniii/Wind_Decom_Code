	PROGRAM MODEL_TABLE
C
C	MAKES A TABLE FOR PLOTTING BY MONGO
C
	integer*4	NMODEL(1100)
	real		MODELAV(1100),MODELSQ(1100),VDATA(1)
C	
	DATA TWOPI /6.2831853/
	DATA IREADIN /0/
C
C	READ IN ACCUMULATED DATA 
C
	IF(IREADIN.EQ.0) THEN
	  OPEN(UNIT=101,NAME='NMODEL.DAT',TYPE='OLD')
	  READ(101,*,END=11) NMODEL
 11	  CLOSE(UNIT=101)
	  OPEN(UNIT=101,NAME='MODELAV.DAT',TYPE='OLD')
	  READ(101,*,END=12) MODELAV
 12	  CLOSE(UNIT=101)
	  OPEN(UNIT=101,NAME='MODELSQ.DAT',TYPE='OLD')
	  READ(101,*,END=13) MODELSQ
 13	  CLOSE(UNIT=101)
	  IREADIN = 1
	ENDIF
C
C	PROCESS ACCUMULATED DATA
C
	OPEN(UNIT=101,NAME='MODEL_TABLE.DAT',TYPE='NEW')
	DO INDEX = 1,1098
C
	  TAV = MODELAV(INDEX)
	  TSQ = MODELSQ(INDEX) - TAV**2
	  ANGLE = (INDEX-1)*360./1098.
	  WRITE(101,*) INDEX,ANGLE,TAV,TSQ
	ENDDO
 	CLOSE(UNIT=101)
	STOP
	END

